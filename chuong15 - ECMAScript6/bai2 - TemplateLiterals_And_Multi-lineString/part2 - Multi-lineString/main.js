// Multi-lineString
//Cách viết thông thường
const lines1 = 'line1.1\nline1.2\nline1.3';
console.log(lines1);
// line1.1
// line1.2
// line1.3

const lines2 = 'line2.1\n'
   + 'line2.2\n'
   + 'line2.3\n';
console.log(lines2);
// line2.1
// line2.2
// line2.3

// Cách dùng Multi-lineString
const lines3 = `line3.1
line3.2
line3.3`;
console.log(lines3);
// line3.1
// line3.2
// line3.3
