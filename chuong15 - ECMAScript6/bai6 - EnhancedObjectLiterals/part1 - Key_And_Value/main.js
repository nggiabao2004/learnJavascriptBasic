// Enhanced object literals
// Định nghĩa key:value cho object

//Cách thông thường
var name1 = 'Javascript';
var price1 = 120;
var course1 = {
   name: name1,
   price: price1
};
console.log(course1);
//{name: 'Javascript', price: 120}

//Cách khi dùng key:value
var name2 = 'Java';
var price2 = 150;
var course2 = {
   name2,
   price2
};
console.log(course2);
//{name2: 'Java', price2: 150}
