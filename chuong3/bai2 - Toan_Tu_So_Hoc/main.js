//Toan tu So hoc
//+ -> Cong
var a= 3;
var b= 6;
console.log(a+b);

//- -> Tru
var a= 3;
var b= 6;
console.log(b-a);
console.log(a-b);

//* -> Nhan
var a= 3;
var b= 6;
console.log(a*b)

//** -> Luy thua
var a= 3;
var b= 6;
console.log(b**a)

// "/" -> Chia nguyen
var a= 3;
var b= 6;
console.log(b/a);
console.log(a/b);

// "%" -> Chia lay du
var a= 3;
var b= 6;
console.log(b%a);
console.log(a%b);

//++ -> Tang 1 gia tri
var a= 3;
a++
console.log(a);

//-- -> Giam 1 gia tri
var b= 6;
b--
console.log(b);
